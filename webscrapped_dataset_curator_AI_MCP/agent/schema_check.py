"""
schema_check.py

Runtime validator for the three target record shapes (pretrain/sft/grpo).
This is deliberately NOT generated by the LLM and NOT reimplemented per
dataset -- it is the one trusted piece of code that both:

  1. the pre-flight test harness (test_gen.py) uses to grade a generated
     map_row() function against sample rows before any real run starts, and
  2. the execution harness (harness.py) uses as a runtime safety net on
     every single row of the real run, so a row that slips past whatever
     the model wrote still can't reach a shard file malformed.

Keeping this in one hand-written module means "does this record match the
schema" is answered identically everywhere, instead of each generated
script re-deriving its own (possibly buggy) notion of what's required.
"""

from __future__ import annotations

from typing import Optional

REQUIRED_FIELDS = {
    "pretrain": {"text", "source", "category"},
    "sft": {"prompt", "thinking", "answer", "source", "category"},
    "grpo": {"prompt", "answer", "source", "category"},
}

# Fields that must be non-empty after stripping whitespace (thinking is
# allowed to be "" -- an empty chain-of-thought is a valid, expected value,
# not a bug).
_NON_EMPTY_FIELDS = {
    "pretrain": {"text", "source", "category"},
    "sft": {"prompt", "answer", "source", "category"},
    "grpo": {"prompt", "answer", "source", "category"},
}


def validate_record(record: object, mode: str) -> tuple:
    """Returns (ok: bool, reason: Optional[str]). `reason` is None on
    success, else a short machine-readable tag naming exactly what's wrong
    -- used both to fail a pytest assertion with a useful message and to
    feed a repair prompt back to the LLM."""
    if mode not in REQUIRED_FIELDS:
        return False, f"unknown_mode:{mode}"
    if record is None:
        return False, "record_is_none"  # caller should treat None as "row intentionally skipped", not an error
    if not isinstance(record, dict):
        return False, f"not_a_dict:{type(record).__name__}"

    required = REQUIRED_FIELDS[mode]
    missing = required - record.keys()
    if missing:
        return False, f"missing_fields:{sorted(missing)}"

    for field in required:
        value = record[field]
        if not isinstance(value, str):
            return False, f"field_not_str:{field}={type(value).__name__}"

    for field in _NON_EMPTY_FIELDS[mode]:
        if not record[field].strip():
            return False, f"field_empty:{field}"

    extra = record.keys() - required - {"_path"}
    if extra:
        # Not fatal -- generated code may pass through harmless extra
        # metadata -- but worth surfacing so a repair prompt can tell the
        # model to stop inventing fields it wasn't asked for.
        return True, f"warn_unexpected_fields:{sorted(extra)}"

    return True, None


def is_skip(result) -> bool:
    """True if map_row's return value (None) represents an intentional
    'this row doesn't belong in the dataset' decision rather than a schema
    violation."""
    return result is None
