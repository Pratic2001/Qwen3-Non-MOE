#!/usr/bin/env python3
"""
harness.py

The trusted, hand-written "loop" that used to be regenerated by an LLM call
for every single dataset. codegen_pipeline.py now only asks the model for a
small pure function:

    def map_row(row: dict) -> Optional[dict]:
        ...

which maps ONE source row (an HF dataset row, or a {"url","text"} raw-crawl
row) to the target record shape, or returns None to skip it. Everything
else -- streaming, quality filtering, exact-dedup, shard writing, progress
reporting, and the final RESULT_JSON line -- lives here, once, reviewed and
unit-tested (see agent/tests/test_harness.py), instead of being reinvented
(and occasionally miswritten) by Ollama on every run.

Why this matters: the class of bug that hurts most in this pipeline is a
STRUCTURAL one in the loop itself (off-by-one shifts, a duplicated-write
path, an exhaustion check that never fires, a wrong load_dataset kwarg) --
not a bad column-name guess. Column-name guesses are easy for both a human
and an LLM to get right and easy to unit-test against real sample rows
before a full run starts. Loop/streaming bugs are exactly the kind that
silently produce garbage at full scale while looking fine on the first few
printed lines. Shrinking the LLM's surface area down to "one pure function,
no I/O, no loop, no argparse" removes almost the entire class of bug this
pipeline has actually been bitten by.

Contract for the generated map_row module:
    - Must define `map_row(row: dict) -> Optional[dict]`.
    - Pure: no file I/O, no network calls, no global mutable state beyond
      what's needed for the mapping itself (a couple of module-level regex
      constants are fine; opening files or making requests is not).
    - Returns None to skip a row it can't/shouldn't map.
    - Returns a dict with (at least) the schema.REQUIRED_FIELDS[mode] keys,
      all strings. May include "_path" (used only for the code-quality
      filter's extension check; stripped before writing).
    - May raise -- the harness catches per-row exceptions, counts them, and
      aborts the whole run as a FATAL error (not a silent skip) if the
      error rate is high enough to indicate a systemic bug rather than a
      handful of malformed source rows.
"""

from __future__ import annotations

import argparse
import importlib.util
import itertools
import json
import os
import sys
import time
import traceback
from typing import Iterator, Optional

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
from quality import (
    ExactDedup, ShardWriter,
    passes_prose_quality_filter, passes_sft_pair_quality_filter,
    passes_code_quality_filter,
)
from schema_check import validate_record

PROGRESS_INTERVAL_SECONDS = 5.0

# If more than this fraction of the first MIN_ROWS_FOR_ERROR_RATE rows
# either raise or fail schema validation, treat it as a systemic map_row
# bug (wrong column name used unconditionally, wrong return shape, etc.)
# and abort with FATAL rather than silently limping through a mostly-empty
# run. This is the harness's own "test that keeps running during the real
# run", catching the case where the pre-flight sample test passed (maybe
# the model got lucky on 8 rows) but the function is still broken at scale.
MIN_ROWS_FOR_ERROR_RATE_CHECK = 200
MAX_ERROR_RATE = 0.9
# A hard, absolute cap too, so a bug isn't given 200 rows of rope on a tiny
# dataset that will exhaust well before that -- if we see this many
# consecutive failures with zero successes, stop immediately.
MAX_CONSECUTIVE_FAILURES_WITH_NO_SUCCESS = 50


class FatalHarnessError(Exception):
    pass


def load_map_row(module_path: str):
    spec = importlib.util.spec_from_file_location("_generated_map_row", module_path)
    if spec is None or spec.loader is None:
        raise FatalHarnessError(f"could not load module spec from {module_path}")
    module = importlib.util.module_from_spec(spec)
    try:
        spec.loader.exec_module(module)
    except Exception as e:
        raise FatalHarnessError(f"map_row module raised on import: {e!r}\n{traceback.format_exc()}")
    fn = getattr(module, "map_row", None)
    if fn is None or not callable(fn):
        raise FatalHarnessError(f"{module_path} does not define a callable map_row(row)")
    return fn


# ---------------------------------------------------------------------------
# Row sources
# ---------------------------------------------------------------------------

def iter_hf_rows(dataset_id: str, config: Optional[str], split: str) -> Iterator[dict]:
    from datasets import load_dataset
    ds = load_dataset(dataset_id, config, split=split, streaming=True)
    for row in ds:
        yield row


def iter_raw_rows(raw_path: str) -> Iterator[dict]:
    with open(raw_path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                yield json.loads(line)
            except json.JSONDecodeError:
                continue


# ---------------------------------------------------------------------------
# Quality gate -- dispatches on mode/category, same bar dataset_agent.py and
# build_dataset.py apply, kept here (not in the generated function) so it
# can never drift per-dataset.
# ---------------------------------------------------------------------------

def _passes_quality(record: dict, mode: str, category: str, min_doc_chars: int) -> tuple:
    if mode == "pretrain":
        if category == "code":
            ok, reason = passes_code_quality_filter(
                record["text"], record.get("_path", ""), min_doc_chars=min_doc_chars)
        else:
            ok, reason = passes_prose_quality_filter(record["text"], min_doc_chars=min_doc_chars)
    else:  # sft, grpo
        ok, reason = passes_sft_pair_quality_filter(record["prompt"], record["answer"])
    return ok, reason


def _dedup_key(record: dict, mode: str) -> str:
    return record["text"] if mode == "pretrain" else f"{record['prompt']}\n\n{record['answer']}"


def _clean_for_write(record: dict) -> dict:
    return {k: v for k, v in record.items() if k != "_path"}


# ---------------------------------------------------------------------------
# Main run loop
# ---------------------------------------------------------------------------

def run(map_row_fn, rows: Iterator[dict], mode: str, category: str, out_dir: str,
        min_doc_chars: int, target_bytes: int, dedup_persist_path: Optional[str] = None) -> dict:
    writer = ShardWriter(out_dir, category)
    dedup = ExactDedup(persist_path=dedup_persist_path)

    total_seen = 0
    total_map_errors = 0
    total_schema_rejects = 0
    total_quality_rejects = 0
    total_dupes = 0
    consecutive_failures = 0
    last_progress = time.monotonic()
    fatal = None

    try:
        for row in rows:
            total_seen += 1
            try:
                record = map_row_fn(row)
            except Exception as e:
                total_map_errors += 1
                consecutive_failures += 1
                if (consecutive_failures >= MAX_CONSECUTIVE_FAILURES_WITH_NO_SUCCESS
                        and writer.total_docs == 0):
                    fatal = (f"map_row raised on {consecutive_failures} consecutive rows "
                             f"with zero successful rows written -- systemic bug, not bad "
                             f"data. Last error: {e!r}")
                    break
                if (total_seen >= MIN_ROWS_FOR_ERROR_RATE_CHECK
                        and total_map_errors / total_seen > MAX_ERROR_RATE):
                    fatal = (f"map_row error rate {total_map_errors}/{total_seen} exceeds "
                             f"{MAX_ERROR_RATE:.0%} -- systemic bug. Last error: {e!r}")
                    break
                continue

            if record is None:
                consecutive_failures = 0  # an intentional skip is not a failure
                continue

            ok, reason = validate_record(record, mode)
            if not ok:
                total_schema_rejects += 1
                consecutive_failures += 1
                if (consecutive_failures >= MAX_CONSECUTIVE_FAILURES_WITH_NO_SUCCESS
                        and writer.total_docs == 0):
                    fatal = (f"map_row produced {consecutive_failures} consecutive "
                             f"schema-invalid records with zero successful rows written: {reason}")
                    break
                continue
            if reason:  # warn_unexpected_fields -- passed, but log once in a while
                pass

            consecutive_failures = 0

            q_ok, q_reason = _passes_quality(record, mode, category, min_doc_chars)
            if not q_ok:
                total_quality_rejects += 1
                continue

            if dedup.is_duplicate(_dedup_key(record, mode)):
                total_dupes += 1
                continue

            writer.write(_clean_for_write(record))

            if writer.total_bytes >= target_bytes:
                print(f"target reached: {writer.total_bytes} bytes >= {target_bytes}")
                break

            now = time.monotonic()
            if now - last_progress >= PROGRESS_INTERVAL_SECONDS:
                last_progress = now
                print(f"progress: {writer.total_bytes}/{target_bytes} bytes, "
                      f"{writer.total_docs} docs, seen={total_seen}, "
                      f"map_errors={total_map_errors}, schema_rejects={total_schema_rejects}, "
                      f"quality_rejects={total_quality_rejects}, dupes={total_dupes}")
        else:
            print("row source exhausted before target size was reached")
    except Exception as e:
        fatal = f"unhandled exception in harness run loop: {e!r}\n{traceback.format_exc()}"

    writer.close()
    dedup.close()

    result = {
        "actual_bytes": writer.total_bytes,
        "docs": writer.total_docs,
        "rows_seen": total_seen,
        "map_errors": total_map_errors,
        "schema_rejects": total_schema_rejects,
        "quality_rejects": total_quality_rejects,
        "dupes": total_dupes,
    }
    if fatal:
        print(f"[FATAL ERROR CAUGHT]: {fatal}")
        result["error"] = fatal
    print("RESULT_JSON:" + json.dumps(result))
    return result


def main():
    parser = argparse.ArgumentParser(description="Trusted execution harness for a generated map_row().")
    parser.add_argument("--map-module", required=True, help="Path to the generated map_row.py file.")
    parser.add_argument("--mode", required=True, choices=["pretrain", "sft", "grpo"])
    parser.add_argument("--source", required=True, choices=["hf", "raw"])
    parser.add_argument("--dataset-id")
    parser.add_argument("--config", default=None)
    parser.add_argument("--split", default="train")
    parser.add_argument("--raw-path")
    parser.add_argument("--target-size-bytes", type=int, required=True)
    parser.add_argument("--out-dir", default="./data")
    parser.add_argument("--category", required=True)
    parser.add_argument("--min-doc-chars", type=int, default=500)
    args = parser.parse_args()

    try:
        map_row_fn = load_map_row(args.map_module)
    except FatalHarnessError as e:
        print(f"[FATAL ERROR CAUGHT]: {e}")
        print("RESULT_JSON:" + json.dumps({"actual_bytes": 0, "docs": 0, "error": str(e)}))
        return

    if args.source == "hf":
        if not args.dataset_id:
            raise SystemExit("--dataset-id required for --source hf")
        rows = iter_hf_rows(args.dataset_id, args.config, args.split)
    else:
        if not args.raw_path:
            raise SystemExit("--raw-path required for --source raw")
        rows = iter_raw_rows(args.raw_path)

    dedup_persist_path = os.path.join(args.out_dir, args.category, ".exact_dedup.hashes")
    run(map_row_fn, rows, args.mode, args.category, args.out_dir,
        args.min_doc_chars, args.target_size_bytes, dedup_persist_path=dedup_persist_path)


if __name__ == "__main__":
    main()
